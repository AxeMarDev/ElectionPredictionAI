// Define StateData interface
interface StateData {
        state: string;
        candidates: string[];
        percentages: number[];
}
function findStateData(stateName:string, ...stateArrays:StateData[][]) {
        const foundStatesData = [];
        for (const stateArray of stateArrays) {
                const stateData = stateArray.find(state => state.state === stateName);
                if (stateData) {
                        foundStatesData.push(stateData);
                }
        }

        return foundStatesData;
}
